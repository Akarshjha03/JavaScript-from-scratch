//Maps - This is similar to objects, but the it knows the sequence of the keys
const map = new Map();
map.set('IN', 'India');
map.set('US', 'United States');
map.set('UK', 'United Kingdom');
map.set('CA', 'Canada');

console.log(map); // Map(4) { 'IN' => 'India', 'US' => 'United States', 'UK' => 'United Kingdom', 'CA' => 'Canada' }