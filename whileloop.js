let index = 0;
while(index < 5) {
    console.log("Current index is: " + index);
    index++;
} // The loop will run as long as the condition is true, and it may not run at all if the condition is false initially         

//while loop on arrays
let fruits = ["apple", "banana", "cherry"];
let i = 0;
while(i < fruits.length) {
    console.log("Current fruit is: " + fruits[i]);
    i++;
} // The loop will iterate through the array as long as the condition is true, and it may not run at all if the array is empty